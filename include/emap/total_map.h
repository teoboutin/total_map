// TOTAL_MAP_VERSION_STRING is DERIVED from the three components by
// stringification rather than written out, so this proves the derivation
// actually holds on YOUR preprocessor: the components are the single source of
// truth, and a consumer printing the string must see exactly the version a
// consumer gating on TOTAL_MAP_VERSION sees.
//
// It PARSES the string rather than comparing it against a second spelling —
// a literal "0.1.0" here would reintroduce exactly the drift the derivation
// removes, and would be the thing most likely to go stale in this file.
constexpr bool versionStringMatchesComponents()
{
    std::string_view s{TOTAL_MAP_VERSION_STRING};
    const std::size_t want[] = {TOTAL_MAP_VERSION_MAJOR, TOTAL_MAP_VERSION_MINOR,
                                TOTAL_MAP_VERSION_PATCH};

    for (std::size_t part = 0; part < 3; ++part) {
        const std::size_t dot = s.find('.');
        const std::string_view field = s.substr(0, dot);
        if (field.empty()) {
            return false;
        }

        std::size_t n = 0;
        for (const char c : field) {
            if (c < '0' || c > '9') {
                return false;
            }
            n = n * 10 + static_cast<std::size_t>(c - '0');
        }
        if (n != want[part]) {
            return false;
        }

        // Exactly three fields: a dot must follow all but the last, and nothing
        // may trail the last. Catches both a truncated string and stray junk.
        const bool last = (part == 2);
        if (last != (dot == std::string_view::npos)) {
            return false;
        }
        if (!last) {
            s.remove_prefix(dot + 1);
        }
    }
    return true;
}
static_assert(versionStringMatchesComponents());

