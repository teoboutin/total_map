// Version of this header. MAJOR/MINOR/PATCH are the SINGLE SOURCE OF TRUTH:
// both TOTAL_MAP_VERSION and TOTAL_MAP_VERSION_STRING are derived from them
// below, so the three spellings cannot drift apart. There is no generation step,
// which is what keeps the header copy-pastable into a project with no build
// system — a supported install path.
//
// CMakeLists.txt carries the same version in project(VERSION) and checks it
// against these three at configure time. That check is the only thing holding
// the two sides together, and it matters because they are published to
// DIFFERENT install paths: find_package() resolves against project(VERSION),
// while a bare header copy has no build system to ask and can only read these
// macros.
//
// TOTAL_MAP_VERSION is ordered and comparable, so a consumer can gate on a
// version the way they would with __cplusplus:
//
//     #if TOTAL_MAP_VERSION >= 10200  // 1.2.0 or newer
#define TOTAL_MAP_VERSION_MAJOR 0
#define TOTAL_MAP_VERSION_MINOR 1
#define TOTAL_MAP_VERSION_PATCH 0
#define TOTAL_MAP_VERSION                                                      \
    (TOTAL_MAP_VERSION_MAJOR * 10000 + TOTAL_MAP_VERSION_MINOR * 100 +         \
     TOTAL_MAP_VERSION_PATCH)

// That encoding is ordered only while MINOR and PATCH each stay below 100. At
// 100 a field carries into the one above it and two distinct versions collide —
// 0.1.100 and 0.2.0 both encode as 200 — which silently breaks every consumer's
// `#if TOTAL_MAP_VERSION >= ...` gate rather than failing loudly. This guards
// THIS library's release process, not any consumer's use: it can only fire on a
// version bump here, and it costs a compile-time integer comparison.
static_assert(TOTAL_MAP_VERSION_MINOR < 100 && TOTAL_MAP_VERSION_PATCH < 100,
    "total_map: VERSION_MINOR and VERSION_PATCH must each stay below 100, or "
    "TOTAL_MAP_VERSION stops being ordered and consumer version gates silently break.");

// Derived, not written out, so it cannot contradict the components above.
//
// The two-step stringify is necessary, not ceremony: the inner macro stringifies
// its argument AS WRITTEN, so the outer one is what forces
// TOTAL_MAP_VERSION_MAJOR to expand to its value first. Both helpers must stay
// defined — TOTAL_MAP_VERSION_STRING is expanded lazily at the point of USE, so
// #undef-ing them here would break it at every call site.
//
// The result is adjacent string literals joined by the usual translation-phase-6
// concatenation, which is a genuine literal for every ordinary use: an
// initializer, a constant expression, a std::string_view. The one difference
// from a hand-written "0.1.0" is that it is not a single preprocessing TOKEN, so
// passing it to a macro that stringifies its own argument yields the unexpanded
// spelling rather than the version.
#define TOTAL_MAP_STRINGIFY_(x) #x
#define TOTAL_MAP_STRINGIFY(x) TOTAL_MAP_STRINGIFY_(x)
#define TOTAL_MAP_VERSION_STRING                                               \
    TOTAL_MAP_STRINGIFY(TOTAL_MAP_VERSION_MAJOR) "."                           \
    TOTAL_MAP_STRINGIFY(TOTAL_MAP_VERSION_MINOR) "."                           \
    TOTAL_MAP_STRINGIFY(TOTAL_MAP_VERSION_PATCH)

