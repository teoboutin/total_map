// ---------------------------------------------------------------------------
// Customization point: number of enumerators of E.
//
// Default: a contiguous enum 0..Count-1 with a trailing `Count` sentinel. This
// is the default because it SELF-MAINTAINS — add an enumerator and N follows —
// which a hand-written count cannot do. See "THE ONE THING IT DOES NOT PROVE"
// at the top: N is trusted either way, and a stale hand-written N fails exactly
// as silently as a misread sentinel.
//
// There are three ways to override it, on two axes: a RULE (reads the enum, so
// it self-maintains) versus a NUMBER (hand-written, so it can go stale), and a
// whole PROJECT versus one ENUM.
//
//                     | a RULE                  | a NUMBER
//      ---------------+-------------------------+------------------------
//      whole project  | 1. enum_count_policy    | —
//      one enum       | 2. enum_count, reading  | 3. enum_count, with a
//                     |    that enum's sentinel |    literal count
//
// Prefer the first that fits. Reach for 3 only when there is no sentinel to
// read: it is the one form that can go stale.
//
// 1. A POLICY — a RULE, for a whole project. If your house sentinel is spelled
//    something other than `Count`, teach it ONCE and every enum follows. No
//    per-enum annotation, no macro, no build-time configuration:
//
//        namespace emap {
//        template <class E>
//            requires (std::is_enum_v<E> && requires { E::Size; })
//        struct enum_count_policy<E>
//            : std::integral_constant<std::size_t,
//                                     static_cast<std::size_t>(E::Size)> {};
//        }
//
//    A policy READS the enum, so like the default it SELF-MAINTAINS and cannot
//    go stale. It is also ADDITIVE: a third-party library whose enums use the
//    stock `Count` sentinel keeps working right beside it. A policy outranks the
//    `Count` rule, so an enum carrying both spellings resolves to the policy
//    rather than being ambiguous.
//
//    Put the policy in a header your enum declarations include. A translation
//    unit that never sees it reads the stock `Count` rule instead — for a
//    sentinel-only enum that is loud (enum_count stays incomplete and the
//    static_assert below fires), but the discipline is worth keeping.
//
//    Specialize enum_count_policy ONLY with a constrained partial specialization
//    — a rule. An EXPLICIT enum_count_policy<E>, for one enum, also works and
//    even outranks a project policy, but form 2 below says the same thing more
//    directly; prefer it.
//
// 2. AN EXPLICIT SPECIALIZATION READING THE SENTINEL — a RULE, for one enum.
//    When a single enum's sentinel is spelled its own way, point the trait at
//    it. Same shape as specializing std::tuple_size, so the ::value member and
//    the _v alias are where you expect:
//
//        namespace app { enum class Dir { North, East, South, West, DirCount }; }
//
//        namespace emap {
//        template <> struct enum_count<app::Dir>
//            : std::integral_constant<std::size_t,
//                                     static_cast<std::size_t>(app::Dir::DirCount)> {};
//        }
//
//    The static_cast is REQUIRED, not decoration: integral_constant's second
//    argument is a std::size_t, and a scoped enum will not convert to one
//    implicitly. Writing `app::Dir::DirCount` bare is a compile error.
//
//    This reads the enum, so it SELF-MAINTAINS exactly like a policy or the
//    default: add an enumerator and N follows. Prefer it over form 3 whenever
//    the enum has any sentinel at all. It is the right choice for a one-off
//    (a lone enum whose sentinel matches nothing else in the project), and for
//    an enum whose sentinel varies per enum — `NUM_COLORS`, `kDirCount` — which
//    no single policy can name.
//
//    An explicit specialization outranks BOTH the policy tier and the `Count`
//    rule, so this is also how you override a project-wide policy for one enum.
//
// 3. AN EXPLICIT SPECIALIZATION WITH A COUNT — a NUMBER, for one enum. The last
//    resort, for an enum with NO usable sentinel to read:
//
//        namespace emap {
//        template <> struct enum_count<MyEnum>
//            : std::integral_constant<std::size_t, /* number of enumerators */> {};
//        }
//
//    Two cases need it:
//      a. No sentinel at all: `enum class Dir { North, East, South, West };`
//      b. The only trailing enumerator is A REAL VALUE IN ITS OWN RIGHT, so
//         reading it would be wrong — e.g. `enum class Metric { Sum, Count,
//         Mean, Max };` reads N == 1, and `enum class Agg { Count, Sum, Avg };`
//         reads N == 0.
//    YOU own keeping the number current when the enum grows. This is the ONLY
//    form that can go stale, which is why it is last.
//
// Any override must appear BEFORE the enum's first use with total_map. A
// trait cannot be specialized after it has been implicitly instantiated, so a
// use that comes first permanently locks in the wrong N — with a diagnostic
// about "specialization after instantiation" rather than anything about counts.
// Put the specialization directly under the enum's own declaration, and the
// policy in a header those declarations include, and this cannot arise.
//
// Only `Count` is recognised by DEFAULT — not COUNT, kCount, _Count or any other
// spelling. That is deliberate: each extra spelling widens the chance of
// colliding with a real enumerator (COUNT is *more* likely to be a real value
// than Count, in SQL and statistics code), and a collision costs a silently
// under-covered map. One recognised spelling keeps the guess narrow and
// predictable. A policy lets a project opt into a second spelling FOR ITSELF
// without widening the guess for anyone who did not ask.
//
// (The enum must still be contiguous starting at 0.)
// ---------------------------------------------------------------------------
namespace emap
{

// Consumer tier. Primary is deliberately UNDEFINED: a consumer claims enums by
// adding a constrained partial specialization (see the block above). The library
// never specializes this itself.
template <class E> struct enum_count_policy;

namespace detail
{
template <class E>
concept has_count_sentinel = requires { E::Count; };
template <class E>
concept has_count_policy = requires { enum_count_policy<E>::value; };
}

template <class E> struct enum_count;

// Tier 1: a consumer policy, when one claims E.
template <class E>
    requires detail::has_count_policy<E>
struct enum_count<E> : std::integral_constant<std::size_t, enum_count_policy<E>::value> {
};

// Tier 2: the stock `Count` sentinel — only when no policy claims E.
//
// The `!has_count_policy` term is load-bearing, not belt-and-braces. Without it,
// an enum carrying both spellings matches two partial specializations whose
// constraints are unrelated by subsumption: an ambiguous partial specialization,
// which is a HARD ERROR. It is also not interceptable — the ambiguity fires
// during instantiation, outside the immediate context, so it is an error even
// inside has_enum_count's requires-clause and no static_assert can attach a
// friendly message to it. This term makes the two constraints mutually
// exclusive, so exactly one specialization ever matches and the ambiguity cannot
// form. Partial ORDERING between the tiers is never consulted, which is why the
// non-subsumable `!` costs nothing here.
template <class E>
    requires (detail::has_count_sentinel<E> && !detail::has_count_policy<E>)
struct enum_count<E> : std::integral_constant<std::size_t, static_cast<std::size_t>(E::Count)> {
};

template <class E> inline constexpr std::size_t enum_count_v = enum_count<E>::value;

// True iff N is discoverable for E (either a `Count` sentinel or a user
// specialization of enum_count<E> provides ::value). Used for a friendly
// diagnostic instead of a raw "incomplete type" error.
template <class E>
concept has_enum_count = std::is_enum_v<E> && requires { enum_count<E>::value; };

// ---------------------------------------------------------------------------
// entry<E, V> — an authoring row: a key paired with its value. Keys live only
// in the input; total_map validates them and then drops them. Aggregate, so
// `entry{Color::Red, 42}` deduces entry<Color, int>.
// ---------------------------------------------------------------------------
template <class E, class V> struct entry {
    E key;
    V value;
};
template <class E, class V> entry(E, V) -> entry<E, V>;

// ---------------------------------------------------------------------------
// entry_ref<E, V> — what entries() yields: a key by value, a reference to the
// value living in the map. Named members (.key/.value), matching `entry`,
// rather than std::pair's .first/.second.
//
// The conversion to entry<E, V> is not decorative. It is what lets the iterator
// declare an honest value_type (a real value, not a reference-holder) while
// still satisfying indirectly_readable, which requires
// common_reference_with<iter_reference_t&&, iter_value_t&>. std::pair gets this
// from its converting constructor; a bare aggregate has no such conversion and
// fails the concept outright — it would not even model input_iterator. V is
// const-qualified on the const entries() path, hence remove_const_t here.
// ---------------------------------------------------------------------------
template <class E, class V> struct entry_ref {
    E key;
    V& value;
    constexpr operator entry<E, std::remove_const_t<V>>() const { return {key, value}; }
};

namespace detail
{
// Forward iterator over (key, value&) proxies. Storage is indexed by the enum's
// underlying value, so the key for slot i is just static_cast<E>(i) — the same
// recovery key_at() performs. V is const-qualified on the const path.
template <class E, class V> class entry_iterator
{
    V* base_ = nullptr;
    std::size_t i_ = 0;

  public:
    using value_type = entry<E, std::remove_const_t<V>>;
    using reference = entry_ref<E, V>;
    using difference_type = std::ptrdiff_t;
    using iterator_concept = std::forward_iterator_tag;
    // Legacy category stays input_iterator_tag: Cpp17ForwardIterator requires a
    // true reference and ours is a proxy. The C++20 iterator_concept above is
    // what std::ranges actually consults.
    using iterator_category = std::input_iterator_tag;

    entry_iterator() = default;
    constexpr entry_iterator(V* base, std::size_t i) : base_(base), i_(i) {}

    constexpr reference operator*() const { return {static_cast<E>(i_), base_[i_]}; }
    constexpr entry_iterator& operator++()
    {
        ++i_;
        return *this;
    }
    constexpr entry_iterator operator++(int)
    {
        auto tmp = *this;
        ++i_;
        return tmp;
    }
    constexpr bool operator==(const entry_iterator&) const = default;
};

// The range entries() returns. Non-owning: it points into the map, so it
// dangles if the map does.
template <class E, class V> class entry_view
{
    V* base_ = nullptr;
    std::size_t n_ = 0;

  public:
    constexpr entry_view(V* base, std::size_t n) : base_(base), n_(n) {}
    constexpr auto begin() const { return entry_iterator<E, V>{base_, 0}; }
    constexpr auto end() const { return entry_iterator<E, V>{base_, n_}; }
    constexpr std::size_t size() const { return n_; }
};
} // namespace detail

// ---------------------------------------------------------------------------
// total_map
// ---------------------------------------------------------------------------
template <class E, class V> class total_map
{
    static_assert(std::is_enum_v<E>, "emap::total_map: E must be an enum type.");
    static_assert(has_enum_count<E>,
        "emap::total_map: could not determine the number of enumerators of E. Add a "
        "trailing sentinel enumerator (`Count` by default), specialize "
        "emap::enum_count<E>::value, or claim E with an emap::enum_count_policy<E>.");
    static_assert(std::is_copy_constructible_v<V>,
        "emap::total_map: V must be copy-constructible to be placed into storage.");

    static constexpr std::size_t N = enum_count_v<E>;

    // N == 0 means E was read as having no enumerators at all, which no real
    // enum has. The overwhelmingly likely cause is a `Count` enumerator that is
    // a REAL value at position 0, not a trailing sentinel — e.g.
    // `enum class Agg { Count, Sum, Avg }`, where the default trait reads
    // Count == 0 and concludes the enum is empty.
    static_assert(N > 0,
        "emap::total_map: E was read as having zero enumerators. If the sentinel being "
        "read is a real enumerator of your enum rather than a trailing sentinel, "
        "specialize emap::enum_count<E> with the true count.");

    std::array<V, N> data_; // values only — keys were dropped after validation

    // Validate exhaustiveness/uniqueness/range and return, for each enum index,
    // the input slot that holds it. A taken `throw` is not a constant
    // expression, so any violation turns this consteval evaluation into a
    // compile error.
    //
    // There is deliberately NO `M != N` check, and a wrong row count is still
    // rejected — by the checks below, which report it more precisely:
    //
    //   M < N  the missing enumerators reach the coverage loop  -> "not covered"
    //   M > N  M keys cannot be distinct in [0, N), so by pigeonhole one of the
    //          range or uniqueness checks fires first  -> "duplicate enum key"
    //
    // So the accepted set is exactly the same as with an explicit count check;
    // only the diagnostic differs, and each check now names one concept. An
    // earlier `if (M != N)` throw masked the coverage loop entirely (making it
    // unreachable), and answered a forgotten row with advice about `Count` and
    // stale counts — the trust-boundary caveat, aimed at someone who had simply
    // omitted a line.
    template <std::size_t M>
    static consteval std::array<std::size_t, N> make_perm(const std::array<entry<E, V>, M>& in)
    {
        std::array<std::size_t, N> perm{};
        std::array<bool, N> seen{};
        for (std::size_t i = 0; i < M; ++i) {
            const std::size_t idx = static_cast<std::size_t>(in[i].key);
            if (idx >= N) {
                throw "emap::total_map: enum key >= enum_count_v<E>. If that key is a real "
                      "enumerator, N is too small: is the sentinel being read a real "
                      "enumerator of your enum rather than a trailing sentinel, or has a "
                      "hand-written emap::enum_count<E> gone stale? Specialize "
                      "emap::enum_count<E>.";
            }
            if (seen[idx]) {
                throw "emap::total_map: duplicate enum key";
            }
            seen[idx] = true;
            perm[idx] = i;
        }
        for (std::size_t i = 0; i < N; ++i) {
            if (!seen[i]) {
                // Leads with the overwhelmingly common cause (a forgotten row);
                // the stale-N case is the same trust-boundary caveat the
                // out-of-range message carries, in its mirror image: N too
                // LARGE leaves a slot no row can fill. Only a hand-written
                // count can go stale this way — a sentinel, read by the default
                // rule or by a policy, tracks the enumerators by construction.
                throw "emap::total_map: enum value not covered. Some enumerator has no row. "
                      "If your table looks complete, N may be too large: has a hand-written "
                      "emap::enum_count<E> gone stale? Specialize emap::enum_count<E> with "
                      "the true count.";
            }
        }
        return perm;
    }

    // Build data_ from the permutation, copying the VALUE of each row and
    // discarding its key. V is only copy-constructed, so no default constructor
    // is required.
    template <std::size_t M, std::size_t... Is>
    consteval total_map(const std::array<entry<E, V>, M>& in, const std::array<std::size_t, N>& perm,
                        std::index_sequence<Is...>)
        : data_{in[perm[Is]].value...}
    {
    }

  public:
    // Construct from an array holding exactly one entry per enumerator, in any
    // order. consteval => the check runs at compile time on every construction.
    template <std::size_t M>
    consteval total_map(const std::array<entry<E, V>, M>& in)
        : total_map(in, make_perm(in), std::make_index_sequence<N>{})
    {
    }

    // Variadic authoring: `total_map{entry{...}, entry{...}, ...}` with no
    // std::array wrapper. Delegates to the array constructor above, so all
    // validation stays in exactly one place. The same_as constraint keeps this
    // from competing with the array overload (a std::array cannot deduce as
    // entry<E, V>) and turns a mixed-type row into a clear constraint error.
    template <std::same_as<entry<E, V>>... Rows>
    consteval total_map(Rows... rows)
        : total_map(std::array<entry<E, V>, sizeof...(Rows)>{rows...})
    {
    }

    // The key's TYPE is checked by the signature. Its VALUE is checked only in
    // debug (see CAVEATS): a `Count` sentinel used as a live "none" value, or
    // any forged cast, would otherwise read out of bounds. Free under NDEBUG,
    // and constant-expression subscripts are checked regardless.
    //
    // Mutation is safe: only the value is exposed; there is no stored key to
    // corrupt.
    constexpr const V& operator[](E e) const
    {
        assert(static_cast<std::size_t>(e) < N &&
               "emap::total_map: key out of range (a sentinel used as a live value?)");
        return data_[static_cast<std::size_t>(e)];
    }
    constexpr V& operator[](E e)
    {
        assert(static_cast<std::size_t>(e) < N &&
               "emap::total_map: key out of range (a sentinel used as a live value?)");
        return data_[static_cast<std::size_t>(e)];
    }

    // Iteration is over VALUES, in enum order. Both const and mutable, since
    // mutating a value can no longer break any invariant.
    constexpr auto begin() { return data_.begin(); }
    constexpr auto end() { return data_.end(); }
    constexpr auto begin() const { return data_.begin(); }
    constexpr auto end() const { return data_.end(); }
    constexpr auto cbegin() const { return data_.cbegin(); }
    constexpr auto cend() const { return data_.cend(); }

    constexpr V* data() { return data_.data(); }
    constexpr const V* data() const { return data_.data(); }

    static constexpr std::size_t size() { return N; }

    // Keys are not stored, but each is recoverable in O(1) from its slot, since
    // storage is indexed by static_cast<size_t>(key) and E is contiguous from 0.
    // Prefer entries() for iteration; this is the direct form for a single slot.
    // Static, so it is callable on an instance or on the type:
    //     m.key_at(i)
    //     emap::total_map<E, V>::key_at(i)   // generic code with no instance
    static constexpr E key_at(std::size_t i)
    {
        assert(i < N && "emap::total_map: index out of range");
        return static_cast<E>(i);
    }

    // Iterate keys alongside values: `for (auto [k, v] : m.entries())`.
    // Values are references, so mutation through the view works. The view is
    // non-owning — calling entries() on a temporary dangles, as with any view.
    constexpr auto entries() { return detail::entry_view<E, V>{data_.data(), N}; }
    constexpr auto entries() const { return detail::entry_view<E, const V>{data_.data(), N}; }
};

// Deduction guide: deduces E and V from the entry rows, keeping call sites
// clean. `emap::total_map m{std::array{entry{...}, ...}}` needs no factory.
template <class E, class V, std::size_t M>
total_map(const std::array<entry<E, V>, M>&) -> total_map<E, V>;

// Guide for the variadic form: E and V come from the first row; the
// constructor's same_as constraint requires the rest to match.
template <class E, class V, class... Rest>
total_map(entry<E, V>, Rest...) -> total_map<E, V>;

// ---------------------------------------------------------------------------
// Buildability predicate — a compile-time answer to "would total_map(Arr)
// succeed?". True iff the array covers every enumerator exactly once with the
// correct size; false for any table that would be rejected. This makes the
// rejection behavior expressible with static_assert.
//
// ONE concept, two ways to pass the table. Which you use is decided by what your
// V supports, not by picking a different name:
//
//   emap::buildable<Arr>    — BY VALUE; nicest syntax; limited (see below)
//   emap::buildable<&Arr>   — BY POINTER; works for ANY value type
//
// HOW IT WORKS
//   It forces the consteval construction — `total_map(...)`, CTAD on the
//   consteval constructor — into a template-argument position (the operand of
//   std::bool_constant<...>), so a `throw` from make_perm during that evaluation
//   is caught as a *substitution failure* — making the concept false — instead
//   of a hard error.
//
//   The CONSTRUCTION must be the outermost call in that operand. Only the
//   ARGUMENT may be computed by a helper (which is exactly what detail::
//   deref_if_ptr does below, and why the by-pointer form costs nothing). Routing
//   the CONSTRUCTION itself through another callable — wrapping the whole thing
//   in a lambda, say — pushes the throw out of the immediate context.
//
//   That last point is not folklore; it is measured, and IMPLEMENTATIONS
//   DISAGREE. Wrapping the construction in a lambda gives:
//       g++ 13.3    — HARD ERROR, compilation aborts
//       clang++ 22  — a clean `false`, compiles fine
//   So the rule is a real portability constraint, not a style preference: g++
//   will reject what clang accepts. Keep the construction outermost.
//
// PASSING BY VALUE — emap::buildable<Arr> (convenient, but restricted)
//   Passes the ARRAY directly as a non-type template parameter, so Arr must be a
//   valid NTTP. That is STRICTER than "the table builds": both the TYPE and the
//   VALUES must qualify.
//     * The type: entry<E,V> must be STRUCTURAL — V structural, all members
//       public and non-mutable. Plain aggregates and scalars qualify. A value
//       type with private members does NOT: e.g. total_map<E, std::string_view>
//       BUILDS fine, but `emap::buildable<array-of-string_view>` is a HARD
//       ERROR ("not structural"), not a `false`. (std::string is the same.)
//     * The values: even a structural type can hold a value that is not a valid
//       template argument. The canonical case is `const char*` initialized from
//       a string literal: `const char*` is structural, but a pointer NTTP may
//       not point at a string-literal object, so an array of such entries is
//       ALSO a hard error at the point of use.
//   Note both failures are HARD ERRORS in forming this concept-id, upstream of
//   anything this header can intercept; there is no way to turn them into a
//   friendly message here, and C++20 has no reliable way to pre-detect "is T a
//   valid NTTP type" (a would-be structural_type<T> either false-positives on
//   private-member types or false-negatives on non-default-constructible ones).
//   So: use this form for scalar/aggregate value types; for anything else pass
//   by pointer, below.
//
// PASSING BY POINTER — emap::buildable<&Arr> (general)
//   Passes a POINTER to the array. A pointer is a structural NTTP type no matter
//   what it points at, and the array is reached by dereference *inside* the
//   constant expression — so the array's own type and values never face the NTTP
//   rules. This form works for std::string_view, std::string, `const char*`
//   holding string literals, and any other buildable value type, while still
//   returning false for missing / duplicate / wrong-size tables.
//   Its ONE requirement is that &Arr be a constant expression, i.e. Arr must have
//   static storage duration: a namespace-scope constexpr (inline or not) or a
//   `static constexpr` local. A plain block-scope constexpr has automatic
//   storage and is rejected with a clear message of its own ("the address of
//   'Arr' is not a valid template argument because it does not have static
//   storage duration") — so this constraint is self-diagnosing.
//
//   Example:
//       inline constexpr auto labels = std::array{
//           emap::entry{Color::Red, std::string_view{"red"}},
//           emap::entry{Color::Green, std::string_view{"green"}},
//           emap::entry{Color::Blue, std::string_view{"blue"}}};
//       static_assert(emap::buildable<&labels>);       // OK, true
//
// TWO OTHER WAYS to make the convenient by-value form usable for string-like V
//   (if you specifically want emap::buildable<Arr> rather than the pointer
//    form), both keep V structural AND the values NTTP-valid:
//     1. Point `const char*` at NAMED objects with static storage + linkage
//        instead of inline literals:
//            inline constexpr char kRed[]="red", kGreen[]="green", kBlue[]="blue";
//            // emap::entry{Color::Red, +kRed}, ... -> valid template argument
//     2. Store the string BY VALUE in a small structural wrapper with a public
//        char[Cap] member (a fixed-capacity string), so no pointer-to-literal is
//        involved. Pick one Cap large enough for every entry (different literal
//        lengths would otherwise be different types).
// ---------------------------------------------------------------------------
namespace detail
{
// Yields the array itself, or the array a pointer points at. This computes only
// the ARGUMENT — total_map(...) stays the outermost call in the bool_constant
// operand below, which is what keeps a throw from make_perm a substitution
// failure. (Routing the CONSTRUCTION through a callable is the thing that
// breaks, and breaks differently per compiler; see the note above.)
template <class T> constexpr decltype(auto) deref_if_ptr(T x)
{
    if constexpr (std::is_pointer_v<T>) {
        return *x;
    } else {
        return x;
    }
}
} // namespace detail

template <auto X>
concept buildable =
    requires { typename std::bool_constant<(total_map(detail::deref_if_ptr(X)), true)>; };

} // namespace emap

