// MUST-NOT-COMPILE TESTS
// For a structural V, prefer emap::buildable<Arr> above — it expresses the same
// rejection behavior as an ordinary static_assert. This out-of-line driver
// covers the two cases buildable cannot reach:
//
//   1. Non-structural V (std::string_view, ...), where the table cannot be
//      named as a template parameter at all — direct construction is a hard
//      error that aborts the TU, so it cannot be caught by a concept.
//   2. THE VARIADIC FORM. buildable only takes an array, so nothing above can
//      assert that `total_map{entry{...}, entry{...}}` rejects a bad table. The
//      variadic constructor delegates to the array constructor precisely so
//      validation stays in one place; these checks are what prove it still does.
//      Without them, a variadic ctor that skipped make_perm would pass every
//      other test in this header.
//
// Verify each snippet FAILS to compile. Example driver (bash):
//
//   preamble='#include <emap/total_map.h>
//   enum class Color{Red,Green,Blue,Count};'
//
//   check() {  # $1 = rows expected to be rejected (array form OR variadic)
//     printf '%s\nconstexpr emap::total_map m{%s}; int main(){}\n' "$preamble" "$1" > t.cpp
//     g++ -std=c++20 -Iinclude -fsyntax-only t.cpp 2>/dev/null && echo "BUG: compiled" || echo "OK: rejected"
//   }
//
//   # --- array form ---
//   check 'std::array{emap::entry{Color::Red,1},emap::entry{Color::Green,2}}'                                        # missing Blue
//   check 'std::array{emap::entry{Color::Red,1},emap::entry{Color::Red,2},emap::entry{Color::Blue,3}}'               # duplicate Red
