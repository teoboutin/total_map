// entries() actually composes with std::views — not merely modelling
// forward_range, but surviving a real pipe. This header does not include
// <ranges>; the self-test block above does, which is exactly the consumer's
// position. Red (thickness 1) must be filtered out.
constexpr bool entriesComposeWithViews()
{
    int total = 0;
    for (auto [key, value] :
         kBasic.entries() | std::views::filter([](auto e) { return e.value.thickness > 1; })) {
        if (key == Color::Red) {
            return false; // Red should have been filtered out
        }
        total += value.thickness;
    }
    return total == 5; // green(2) + blue(3)
}
