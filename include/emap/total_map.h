// Values iterate in enum order (by key), regardless of authoring order. key_at
// recovers each key from its position, so mutating a value cannot ever make an
// element disagree with its slot (the old failure mode is gone by construction).
constexpr bool iteratesInEnumOrder()
{
    std::size_t i = 0;
    for (const auto& v : kBasic) {
        // slot i must correspond to enum value i; check via the recovered key
        if (emap::total_map<Color, Style>::key_at(i) != static_cast<Color>(i)) {
            return false;
        }
        // and the value at that slot is the one authored for that key
        if (kBasic[static_cast<Color>(i)].thickness != v.thickness) {
            return false;
        }
        ++i;
    }
    return i == kBasic.size();
}
static_assert(iteratesInEnumOrder());

