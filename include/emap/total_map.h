constexpr bool entriesAreInEnumOrder()
{
    std::size_t i = 0;
    for (auto [key, value] : kBasic.entries()) {
        if (key != static_cast<Color>(i)) {
            return false;
        }
        if (value.thickness != kBasic[key].thickness) {
            return false;
        }
        ++i;
    }
    return i == kBasic.size();
}
static_assert(entriesAreInEnumOrder());

