// total_map.h  —  emap::total_map<E, V>
// A compile-time-checked, exhaustive, O(1) map from an enum to values of V.
// Requires C++20.
// ----------------------------------------------------------------------------
//
// WHAT IT IS
//   emap::total_map<E, V> is a fixed-size total map: exactly one V per
//   enumerator of E, stored in a std::array<V, N> indexed by the enum's
//   underlying value. Lookup is a plain array index — no hashing, no branching,
//   no allocation. "Total" is meant in the mathematical sense: the map is a
//   total function from E, defined on every enumerator, and the compiler proves
//   it.
//
//   It is authored from `emap::entry<E, V>` rows, each pairing a key with a
//   value: `entry{Key, Value}`. Rows may be passed directly, or as one
//   std::array (the array form is what emap::buildable<> takes). Construction is
//   `consteval` and REFUSES TO COMPILE unless every enumerator is present
//   exactly once. The keys exist ONLY to validate and place the rows; once
//   validated they are DROPPED. Storage is a bare std::array<V, N> — the key is
//   not kept in each element, so it cannot go stale and costs no space. The key
//   for any slot is recoverable in O(1) from its position, via entries() or
//   key_at() (see below).
//
// INTENDED USE
//   Hand-authored, `constexpr` lookup tables of small/simple data, known in full
//   at compile time, where you want the compiler to guarantee exhaustiveness —
//   e.g. per-enum config: colors, labels, thresholds, dispatch parameters.
//   Author the table as one list of rows and let total_map prove you covered
//   every case. There is no factory: CTAD deduces E and V from the rows.
//
//       enum class Color { Red, Green, Blue, Count };
//       struct Style { int thickness; const char* name; }; // NO key member
//
//       constexpr emap::total_map styles{
//           emap::entry{Color::Red,   Style{1, "red"}},
//           emap::entry{Color::Green, Style{2, "green"}},
//           emap::entry{Color::Blue,  Style{3, "blue"}},  // omit one -> error
//       };
//       static_assert(styles[Color::Green].thickness == 2);
//
//   For a scalar payload no wrapper is needed — the value is stored (and read)
//   directly, with no `.value` indirection:
//
//       constexpr emap::total_map cost{
//           emap::entry{Color::Red, 1250.0}, emap::entry{Color::Green, 500.0},
//           emap::entry{Color::Blue, 625.0},
//       };
//       static_assert(cost[Color::Red] == 1250.0);
//
//   To iterate keys alongside values, use entries(). Values come back by
//   reference, so they can be mutated through the view:
//
//       for (auto [color, style] : styles.entries())
//           use(color, style);
//
//   entries() yields emap::entry_ref<E, V> — named .key/.value members, the
//   same vocabulary as the entry rows you authored with. The view is
//   non-owning: entries() on a temporary dangles, as with any view. The
//   iterator models std::forward_iterator, so if YOU include <ranges> it
//   composes with std::views; this header does not include <ranges> itself.
//
//   It is NOT a runtime container: there is no insert/erase and no runtime
//   construction path. Once built, VALUES may be mutated freely (there is no
//   stored key to desynchronize); the set of keys is fixed. If you need to
//   populate a map at runtime, this is the wrong tool.
//
// REQUIREMENTS (what it relies on to work)
//   * C++20: consteval, concepts/requires-clauses, class template argument
//     deduction, and aggregate CTAD for `entry`.
//   * The enum E must have CONTIGUOUS underlying values 0..N-1. Storage is
//     indexed by static_cast<std::size_t>(key), so a gap or a non-zero base
//     would misindex. This is the key precondition; emap::enum_count (below)
//     only supplies the COUNT N, it does not change the contiguity requirement.
//   * N (the enumerator count) is discovered via emap::enum_count<E>::value,
//     which by default reads a trailing `Count` sentinel (an extra enumerator
//     whose value equals the number of real enumerators). For a project whose
//     sentinel is spelled otherwise, one emap::enum_count_policy teaches the
//     spelling once; for an enum with no sentinel at all, specialize
//     emap::enum_count<E> (both below) — the enum must still be contiguous from
//     0. A missing count is reported with a clear static_assert.
//   * V must be a literal type and copy-constructible (so it is usable in
//     constant expressions and can be placed into storage). It need NOT be
//     default-constructible. Scalars, pointers, and aggregates of literal types
//     all qualify. V does NOT need to carry its own key.
//   * The input array must itself be a constant expression (built from constant
//     data), because construction is consteval.
//
// GUARANTEES
//   * Total: every enumerator has a value (missing one -> compile error).
//   * Unique: no enumerator is supplied twice (duplicate -> compile error).
//   * Correct size: the input array length must equal N (mismatch -> compile
//     error) — a consequence of the two above rather than a separate check: too
//     few rows leave an enumerator uncovered, and too many cannot all be
//     distinct and in range. All three are enforced by a `throw` inside the
//     consteval builder: a taken throw is not a constant expression, so it
//     aborts compilation.
//   * O(1) lookup; values are stored in enum order regardless of input order.
//   * Keys are not stored, so no element can ever disagree with its own slot.
//
// THE ONE THING IT DOES NOT PROVE — read this before trusting the word "total"
//   Everything above is proved against N. N ITSELF IS TRUSTED INPUT. C++20 has
//   no reflection, so this header can prove:
//       "your table covers 0..N-1 exactly once"
//   and cannot prove:
//       "N is the number of enumerators of E"
//   If N is wrong and TOO SMALL, a table covering 0..N-1 is accepted while the
//   enumerators from N onward are silently uncovered — the completeness promise
//   degrades quietly. That is the single hole in this design, and it exists no
//   matter how you supply N:
//     * Via the `Count` sentinel: N is wrong if `Count` is a REAL enumerator
//       rather than a trailing sentinel (e.g. `enum class Metric { Sum, Count,
//       Mean, Max }` reads N == 1). Usually loud — authoring the real
//       enumerators trips the out-of-range check, whose message names this
//       suspicion — but silent if you happen to author exactly 0..N-1.
//     * Via a hand-written emap::enum_count<E>: N is wrong once it goes STALE,
//       i.e. as soon as anyone adds an enumerator and forgets to bump it. A
//       stale count too LARGE is loud in the other direction — no row can cover
//       the surplus slot, so the coverage check fires and its message names
//       this suspicion too.
//   A sentinel is preferred precisely because it SELF-MAINTAINS: add an
//   enumerator and N follows automatically, so the stale case cannot arise. Any
//   override that READS a sentinel keeps that property — an
//   emap::enum_count_policy for a project, or an emap::enum_count specialization
//   pointed at one enum's own sentinel. Only a hand-written NUMBER can go stale,
//   which is why it is the last resort.
//   Rule of thumb: use the trailing `Count` sentinel. If your house sentinel is
//   spelled otherwise, write one emap::enum_count_policy; if a single enum
//   spells its own, point an emap::enum_count specialization at that sentinel.
//   Hand-write a number only for an enum with no sentinel to read.
//
// WHAT FAILS TO COMPILE (by design)
//   * An input array missing any enumerator.
//   * An input array containing a duplicate key.
//   * An input array whose length != N.
//   * A key whose static_cast<size_t> is >= N.
//   Direct construction of such a table is a hard compile error. Separately,
//   whether construction WOULD fail is itself a compile-time predicate,
//   emap::buildable, which takes the table two ways (see its block below for
//   full details):
//     * emap::buildable<Arr>  — by value; cleanest syntax, but needs Arr to be
//       a valid non-type template parameter (structural type AND NTTP-valid
//       values). A `const char*` from a string literal, or a non-structural V
//       like std::string_view, is a hard error here even though the table
//       builds.
//     * emap::buildable<&Arr> — by pointer; works for ANY buildable value type
//       (string_view, literal strings, ...). Requires only that Arr have static
//       storage duration so &Arr is a constant expression.
//   For non-structural V where you cannot name the table, fall back to the
//   must-not-compile driver at the very bottom of this file.
//
// CAVEATS
//   * operator[] takes E, so the KEY'S TYPE is checked at compile time (an int,
//     or another enum, will not convert). Its VALUE is not: a runtime E forged
//     outside 0..N-1 — by a cast, or by a `Count` sentinel used as a live
//     "none/all" value — indexes out of bounds. A debug-only assert catches
//     that (compiled away under NDEBUG); in release the contract is an array's.
//     Constant-expression subscripts are fully checked either way.
//   * No reflection in C++20 means "all enumerators" is inferred from N +
//     contiguity, not read from the language. Under C++26 static reflection the
//     enum_count/Count convention becomes unnecessary.
// ============================================================================

