//
//   # --- variadic form: same guarantees, different construction path ---
//   check 'emap::entry{Color::Red,1},emap::entry{Color::Green,2}'                                        # missing Blue
//   check 'emap::entry{Color::Red,1},emap::entry{Color::Red,2},emap::entry{Color::Blue,3}'               # duplicate Red
